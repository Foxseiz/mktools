var brandDrpL = document.getElementById("devicelist"),
	  catDrpL   = document.getElementById("categorylist"),
    partDrpL  = document.getElementById("partlist"),
    priceTextL = document.getElementById("pricelist"),
    selectedValueL = "";
    
function updateDrpL(element, type)
{
    if (element.length > 0) {
    		selectedValueL = element.value;
        // console.log(selectedValue);
        if (type == 'brand') {
        		/* Call for model. */
            updateModelL();
            /* Call for part. */
            updatePartL();
            /* Call for price. */
            updatePriceL();
        } else if (type == 'cat') {
        		/* Append price. */
            updatePriceL();
        } else if (type == 'part') {
        		/* Append price. */
            updatePriceL();
        }
    }
}

function updateModelL()
{
		let drpOptionsModel = "<option value='' disabled selected>Select Model</option>";
  	for (let key in brandByCategoryPList) {
    		if (brandByCategoryPList.hasOwnProperty(key) && selectedValueL == key) {
      			let drpValues = brandByCategoryPList[key];
      			if (drpValues.length > 0) {
        				for (let option in drpValues) {
         						drpOptionsModel += "<option value="+option+">" + drpValues[option]["name"] + "</option>";
        				}
      			}
      		break;
    		}
  	}

  	if (drpOptionsModel.length > 0) {
    		catDrpL.innerHTML = drpOptionsModel;
		}
}

function updatePartL()
{
  	let drpOptionsPart = "<option value='' disabled selected>Select Part</option>";
  	for (let key in partByCategoryPList) {
    		if (partByCategoryPList.hasOwnProperty(key) && selectedValueL == key) {
      			let drpValues = partByCategoryPList[key];
      			if (drpValues.length > 0) {
        			for (let partInfo in drpValues) {
          				if (Object.keys(drpValues[partInfo]).length > 0) {
            					for (let option in drpValues[partInfo]) {
              						drpOptionsPart += "<option value="+option+">" + drpValues[partInfo][option] + "</option>";
            					}
          				}
        			}
      			}
      			break;
    		}
  	}

  	if (drpOptionsPart.length > 0) {
    		partDrpL.innerHTML = drpOptionsPart;
  	}
}

function updatePriceL()
{
		priceTextL.value = "";
		if (Object.keys(priceByCategoryPList[brandDrpL.value]).length > 0) {
    		for (let modelKey in priceByCategoryPList[brandDrpL.value]) {
        		if (catDrpL.value == modelKey) {
        				for (let partKey in priceByCategoryPList[brandDrpL.value][modelKey]) {
            				if (partDrpL.value == partKey) {
                    		priceTextL.value = priceByCategoryPList[brandDrpL.value][modelKey][partKey];
                    }
            		}
            }
        }
    }
}
